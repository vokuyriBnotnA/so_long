#include "so_long.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char	*res;
	int		i;

	res = malloc(ft_strlen((char *) s1) + ft_strlen((char *) s2) + 1);
	if (!res)
		return (NULL);
	i = 0;
	while (*s1)
	{
		res[i] = *s1;
		i++;
		s1++;
	}
	while (*s2)
	{
		res[i] = *s2;
		i++;
		s2++;
	}
	res[i] = 0;
	return (res);
}
